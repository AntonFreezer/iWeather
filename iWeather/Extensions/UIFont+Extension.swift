import UIKit

extension UIFont {
    static func poppinsRegular(ofSize: CGFloat) -> UIFont {
        UIFont(name: "Poppins-Regular", size: ofSize)!
    }

    static func poppinsMedium(ofSize: CGFloat) -> UIFont {
         UIFont(name: "Poppins-Medium", size: ofSize)!
    }

    static func poppinsSemiBold(ofSize: CGFloat) -> UIFont {
         UIFont(name: "Poppins-SemiBold", size: ofSize)!
    }
    
    static func robotoRegular(ofSize: CGFloat) -> UIFont {
         UIFont(name: "Roboto-Regular", size: ofSize)!
    }

}
